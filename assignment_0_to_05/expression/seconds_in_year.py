# This program calculates the number of seconds in a year.

# Defining constants
days_in_year = 365  # Number of days in a year
hours_in_day = 24  # Number of hours in a day
minutes_in_hour = 60  # Number of minutes in an hour
seconds_in_minute = 60  # Number of seconds in a minute

# Calculating total seconds in a year
seconds_in_year = days_in_year * hours_in_day * minutes_in_hour * seconds_in_minute  # Multiplication of all constants

# Printing the result
print(f"There are {seconds_in_year} seconds in a year!")
