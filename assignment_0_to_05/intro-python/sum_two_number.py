# question 1 sum two number
# This program takes two integer inputs from the user and calculates their sum.

# Prompting the user to enter the first number
first_number = int(input("Enter the first number: "))  # Read input and convert to integer

# Prompting the user to enter the second number
second_number = int(input("Enter the second number: "))  # Read input and convert to integer

# Calculating the sum of both numbers
total_sum = first_number + second_number  # Adding the two numbers

# Printing the total sum with an appropriate message
print(f"The sum of {first_number} and {second_number} is: {total_sum}")
